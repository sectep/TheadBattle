package threadbattle;

// A class for using combat skills.
class Combat {
    // a method, which uses the attack skills.
    public static void useAttack(String name) {
        System.out.println(name + " attacks");
    }

    // a method, which reloads the attack skills.
    public static void useReload(String name) {
        System.out.println(name + " reloads");
    }
}